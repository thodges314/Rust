fn main() {
    let ints = [1, 2, 3];
    let floats = [1.1, 2.2, 3.3];
    let strings = ["hello", "world"];
    let ints_ints = [[1, 2], [30, 40]];

    println!("ints: {:?}", ints);
    println!("floats: {:?}", floats);
    println!("strings: {:?}", strings);
    println!("ints_ints: {:?}", ints_ints);
}
